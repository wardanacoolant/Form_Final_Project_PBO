    private void ID_nasabahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ID_nasabahActionPerformed
        try{

            Connection c = KoneksiDatabase.getKoneksi();
            String sql = "SELECT id_nasabah FROM nasabah " + " ORDER BY id_nasabah DESC LIMIT 1";
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            while(r.next()){
                ID_nasabah.setText(r.getString("id_nasabah"));
            }
        }
        catch(SQLException e){
            System.out.println("Terjadi Error Load"+e.getMessage());
        }
    }//GEN-LAST:event_ID_nasabahActionPerformed

